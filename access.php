           <h2>สำรองฐานข้อมูลของคุณใน Access</h2>
            <p>1 เลือก ไฟล์ > บันทึกเป็น</p>
            <p>2 ภายใต้ ชนิดไฟล์ฐานข้อมูล เลือก บันทึกฐานข้อมูลเป็น</p>
            <p>3 ภายใต้ ขั้นสูง เลือก สำรองฐานข้อมูล แล้วเลือก บันทึกเป็น

                ชื่อไฟล์เริ่มต้นจะประกอบด้วยฐานข้อมูลต้นฉบับและเวลาที่การสำรองข้อมูลเกิดขึ้น</p>
