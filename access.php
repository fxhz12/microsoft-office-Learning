        <h2>Ribbon ของ access</h2>
        <p>Ribbon คือการแทนที่เมนูและแถบเครื่องมือหลัก และมีส่วนติดต่อของสั่งหลักใน Access หนึ่งในข้อดีหลักของ Ribbon</p>
        <p>คือ Ribbon จะรวมข้อมูลในที่เดียว งานหรือจุดเข้าเหล่านั้นที่ใช้เพื่อต้องการเมนู แถบเครื่องมือ บานหน้าต่างงาน และ</p>
        <p>คอมโพเนนต์ UI อื่นๆ เพื่อแสดง ด้วยวิธีนี้ คุณจะมีที่เดียวที่จะค้นหาสั่ง แทนที่จะค้นหาหลายสถานที่</p>
        <p>เมื่อคุณเปิดฐานข้อมูล Ribbon จะปรากฏที่ด้านบนของหน้าต่าง Access หลัก ซึ่งจะแสดงสั่งต่างๆ ในแท็บสั่งที่ใช้งานอยู่</p>
        <img src="mig/iiiiiii.gif" alt="Ribbon" class="responsive-image">
        <p>Ribbon มีชุดของแท็บสั่งที่มีสั่ง ใน Access แท็บสั่งหลักคือ ไฟล์หน้าแรกสร้างข้อมูลภายนอกและเครื่องมือฐานข้อมูล แต่ละแท็บจะมีกลุ่มของสั่งที่เกี่ยวข้องกัน และกลุ่มเหล่านี้แสดงองค์ประกอบ UI ใหม่เพิ่มเติม บางส่วน เช่น แกลเลอรี ซึ่งเป็นตัวควบคุมชนิดใหม่ที่เสนอตัวเลือกที่มองเห็นได้</p>
<p>การสั่งที่พร้อมใช้งานบน Ribbon ยังแสดงวัตถุที่ใช้งานอยู่ในปัจจุบันด้วย ตัวอย่างเช่น ถ้าคุณเปิดตารางในมุมมองแผ่นข้อมูลแล้วคุณคลิก
     ฟอร์ม บนแท็บสร้างในกลุ่มฟอร์มAccess จะสร้างฟอร์มโดยยึดตามตารางที่ใช้งานอยู่ นั่นคือ ชื่อของตารางที่ใช้งานอยู่จะถูกใส่ในคุณสมบัติ
      RecordSource ของ ฟอร์มใหม่ นอกจากนี้ แท็บ Ribbon บางแท็บจะปรากฏในบริบทบางอย่างเท่านั้น ตัวอย่างเช่น แท็บ ออกแบบ 
      จะปรากฏเฉพาะเมื่อคุณเปิดวัตถุในมุมมองออกแบบเท่านั้น</p>
      <p>คุณสามารถใช้แป้นพิมพ์ลัดกับ Ribbon ได้ แป้นพิมพ์ลัดทั้งหมดจาก Access เวอร์ชันก่อนหน้าจะยังคงสามารถใช้งานต่อไปได้ ระบบการเข้าถึงแป้นพิมพ์จะแทนที่ตัวเร่งเมนูจาก Access เวอร์ชันก่อนหน้า ระบบนี้ใช้ตัวบ่งชี้ขนาดเล็กที่มีตัวอักษรเพียงตัวเดียวหรือตัวอักษรผสมเดียวที่ปรากฏบน Ribbon เมื่อคุณกดแป้น ALT
         ตัวบ่งชี้เหล่านี้แสดงสิ่งที่แป้นพิมพ์ลัดเปิดใช้งานตัวควบคุมอยู่ข้างใต้</p>
         <h2>เอกสารในแท็บ</h2>
         <p>คุณสามารถใช้ส่วนติดต่อของเอกสารแบบแท็บเพื่อการใช้งานแบบโต้ตอบทุกวันได้ คุณสามารถเปิดหรือปิดใช้งานเอกสารในแท็บได้ด้วยการตั้งค่าตัวเลือกของ Access ของคุณ ( ดู แสดงหรือซ่อนแท็บเอกสาร ภายหลังในบทความนี้) อย่างไรก็ตาม ถ้าคุณเปลี่ยนการตั้งค่าเอกสารในแท็บ
             คุณต้องปิดและเปิดฐานข้อมูลของคุณใหม่เพื่อให้การตั้งค่าใหม่มีผล</p>
             <img src="mig/oooooooooooooooooooooooooooooooooo.gif" alt="Ribbon" class="responsive-image">
<h2>แถบเครื่องมือขนาดเล็ก</h2>
<P>คุณสามารถจัดรูปแบบข้อความได้ง่ายขึ้นโดยใช้แถบเครื่องมือขนาดเล็ก เมื่อคุณเลือกข้อความเพื่อจัดรูปแบบ แถบเครื่องมือขนาดเล็กจะปรากฏขึ้นเหนือข้อความที่เลือกโดยอัตโนมัติ ถ้าคุณย้ายตัวชี้เมาส์ไปใกล้กับแถบเครื่องมือขนาดเล็ก แถบเครื่องมือขนาดเล็กจะค่อยๆ ปรากฏและคุณสามารถใช้เพื่อปรับใช้ตัวหนา ตัวเอียง ขนาดฟอนต์ สี และอื่นๆ เมื่อคุณย้ายตัวชี้ออกจากแถบเครื่องมือขนาดเล็ก แถบเครื่องมือขนาดเล็กจะเลือนหายไป ถ้าคุณไม่ต้องการใช้แถบเครื่องมือขนาดเล็กเพื่อปรับใช้การจัดรูปแบบข้อความกับส่วนที่เลือก 
    เพียงย้ายตัวชี้ไปไม่กี่พิกเซลและแถบเครื่องมือขนาดเล็กจะหายไป</P>
