                <h2>สร้างคิวรี ฟอร์ม</h2>
                <p>เลือก สร้าง > ตัวช่วยสร้างแบบสอบถาม </p>
                <p>เลือก แบบสอบถามอย่างง่าย แล้วเลือก ตกลง</p>
                <p>เลือกตารางที่มีเขตข้อมูล จากนั้นเพิ่ม เขตข้อมูลที่มี ที่คุณต้องการลงใน เขตข้อมูลที่เลือก แล้วเลือก ถัดไป</p>
                <p>เลือกว่าคุณต้องการเปิดแบบสอบถามในมุมมองแผ่นข้อมูลหรือปรับเปลี่ยนแบบสอบถามในมุมมองออกแบบ แล้วเลือก เสร็จสิ้น</p>
                <img src="mig/gggggg.png" alt="สร้างคิวรี">
                <h2>สร้างรายงาน</h2>
                <p>1 เลือก สร้าง > ตัวช่วยสร้างรายงาน</p>
                <p>2 เลือกตารางหรือแบบสอบถาม จากนั้นดับเบิลคลิกที่แต่ละเขตข้อมูลในรายการ 
                    เขตข้อมูลที่มีอยู่ ที่คุณต้องการเพิ่มในรายงาน แล้วเลือก ถัดไป</p>
                    <p>3 ดับเบิลคลิกที่เขตข้อมูลที่คุณต้องการจัดกลุ่ม จากนั้นเลือก ถัดไป</p>
                    <p>4 กรอกส่วนที่เหลือของหน้าจอตัวช่วยสร้างให้เสร็จสมบูรณ์ แล้วเลือก เสร็จสิ้น</p>
