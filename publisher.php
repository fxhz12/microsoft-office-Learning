            <h2>Publisher คืออะไร</h2>
            <p>Publisher เป็นโปรแกรมที่ช่วยสร้างสิ่งพิมพ์ที่มีลักษณะการใช้งานได้ง่ายและรวดเร็วในการสร้าง
                สื่อสิ่งพิมพ์อย่างมืออาชีพด้วย</p>
                <p> Publisher มีความสามารถในการสร้าง การออกแบบ และจัดทำเอกสาร
                    การตลาด ทางการสื่อสารแบบมืออาชีพ สำหรับงานพิมพ์ และการท าจดหมายเวียนหรือจดหมายเวียน
                    อิเล็กทรอนิกส์
                    </p>
                    <h2>สร้างจดหมายข่าวโดยใช้ Publisher</h2>
                    <p>1 คลิก จดหมายข่าว > ที่มีอยู่แล้วภายใน แล้วเลื่อนลงเพื่อค้นหาประเภท
                         จดหมายข่าว</p>
                         <p>2 เลือกเทมเพลต และภายใต้ กําหนดเอง ให้คลิกแบบแผนชุดสีและแบบแผนฟอนต์ที่คุณต้องการ

                         </p>
                         <p>3 คลิกชุดข้อมูลทางธุรกิจที่คุณต้องการ หรือสร้างชุดข้อมูลใหม่</p>
                         <p>4 ภายใต้ ตัวเลือก ให้เลือก แบบหน้าเดียว (ถ้าคุณวางแผนที่จะพิมพ์จดหมายข่าวของคุณแบบหน้าเดียวหรือสองหน้า) หรือ แบบสองหน้า (ถ้าคุณต้องการทํางานกับการออกแบบที่มีหน้าประชิด และคุณวางแผนที่จะ
                             พิมพ์จดหมายข่าวของคุณบนกระดาษขนาด tabloid)</p>
                             <p>5 เลือกกล่องกาเครื่องหมาย 
                                 ถ้าคุณต้องการให้มีที่อยู่ลูกค้าบนจดหมายข่าวสําหรับการส่งจดหมายแทนที่จะอยู่บนซองจดหมายแยกต่างหาก</p>
                                 <p>6 คลิก สร้าง</p>
                                 <p>7 เปลี่ยนเทมเพลตเพื่อรวมสิ่งต่างๆ ที่คุณต้องการนํามาใช้ใหม่ในจดหมายข่าวในอนาคต เช่น ชื่อเรื่องและโลโก้</p>
                                 <p>8 
                                 บันทึกเทมเพลตใหม่ของคุณ และจากกล่องโต้ตอบ บันทึกเป็น ที่ปรากฏขึ้น
                                  ให้นําทางไปยังตําแหน่งที่ตั้งและโฟลเดอร์ที่คุณต้องการ เลือก เทมเพลต Publisher ในกล่อง บันทึกเป็นชนิด</p>
                                  <img src="mig/7777777777.jpg" alt=" Microsoft ">
                                  <h2>สร้างสิ่งพิมพ์</h2>
                                  <p>สิ่งพิมพ์ทั้งหมดจะเริ่มต้นด้วยเทมเพลต ถึงแม้ว่าจะเป็นเทมเพลตเปล่าก็ตาม 
                                    เคล็ดลับคือการค้นหาเทมเพลตที่มีลักษณะเหมือนกับภาพที่คุณมีสําหรับสิ่งพิมพ์ขั้นสุดท้าย Publisher 
                                    มาพร้อมกับเทมเพลตที่มีอยู่แล้วภายใน</p>
                                    <p>1 คลิก ไฟล์ > ใหม่ แล้วเลือกทำดังนี้</p>
                                    <p>2 เลือกเทมเพลต เด่น และคลิก สร้าง</p>
                                    <p>3 เมื่อต้องการใช้หนึ่งในเทมเพลตหนึ่งที่ติดตั้งไว้ใน Publisher ให้เลือก
                                         มีอยู่แล้วภายใน เลื่อนไปคลิกประเภทที่คุณต้องการ เลือกเทมเพลต แล้วคลิก สร้าง</p>
                                         <p>เมื่อต้องการใช้หนึ่งในเทมเพลตหนึ่งที่ติดตั้งไว้ใน Publisher
                                             ให้เลือก มีอยู่แล้วภายใน เลื่อนไปคลิกประเภทที่คุณต้องการ เลือกเทมเพลต แล้วคลิก สร้าง</p>
                                             <img src="mig/llllllllllllllllllllllllllllllllllllllllllllllllllllll.png" alt=" Microsoft ">
                                             <h2>บันทึกสิ่งพิมพ์ของคุณ</h2>
<p>1 คลิก ไฟล์ > บันทึกเป็น</p>
<p>2 ใน บันทึกเป็น ให้เลือกตำแหน่งที่คุณต้องการบันทึกสิ่งพิมพ์ของคุณ</p>
