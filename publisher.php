        ถ้าข้อความที่คุณพิมพ์ยาวเกินไปสำหรับกล่องข้อความ คุณสามารถทำให้กล่องข้อความใหญ่ขึ้น หรือลิงก์ไปยังกล่องข้อความอื่นได้</p>
        <h2>เพิ่มเอกสารสำเร็จรูปลงในสิ่งพิมพ์ของคุณ</h2>
        <p>แบบเอกสารสําเร็จรูปคือส่วนของเนื้อหาที่นํากลับมาใช้ใหม่ได้ เช่น หัวเรื่อง ปฏิทิน 
            เส้นขอบ และโฆษณา Publisher มีแบบเอกสารสําเร็จรูปที่มีอยู่แล้วภายใน หรือคุณสามารถสร้างของคุณเองได้</p>
            <p>1 ในบานหน้าต่าง การนำทางในหน้า ให้เลือกหน้าในสิ่งพิมพ์ของคุณ
            </p>
            <p>2 บน แทรก > แบบเอกสารสำเร็จรูป ให้เลือกแกลเลอรีแบบเอกสารสำเร็จรูป</p>
            <p>3 
            เลื่อนเพื่อค้นหาแบบเอกสารสำเร็จรูป หรือคลิก <ชื่อแกลเลอรี> เพิ่มเติม เพื่อเปิด ไลบรารีแบบเอกสารสำเร็จรูป</p>
            <p>4 คลิกแบบเอกสารสำเร็จรูป</p>
