<h2>เพิ่มข้อความลงในสิ่งพิมพ์ของคุณ</h2>
<p>เพิ่มข้อความลงในสิ่งพิมพ์ของคุณ</p>
<p>ลิก หน้าแรก > วาดกล่องข้อความ และลากเคอร์เซอร์รูปกากบาทเพื่อวาดกล่องที่คุณต้องการข้อความ</p>
<img src="mig/vvvvvvvvvvvvvvvvvvvvvvvv.png" alt=" Microsoft ">
<p>พิมพ์ข้อความในกล่องข้อความ

    ถ้าข้อความที่คุณพิมพ์ยาวเกินไปสำหรับกล่องข้อความ คุณสามารถทำให้กล่องข้อความใหญ่ขึ้น หรือลิงก์ไปยังกล่องข้อความอื่นได้</p>
